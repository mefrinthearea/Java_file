class equality {
  public static void main(String[] args) {
    int number1 = 12;
    int number2 = 15;

    // is equal to
    System.out.println(number1 == number2); //false. 

    //is not equal to
    System.out.println(number1 != number2); //true

    //is greater than
    System.out.println(number1 > number2); //false

    //is less thenn
    System.out.println(number1 < number2); //true

    //is greater than or equal to
    System.out.println(number1 >= number2); //false

    //is less than or equal to
    System.out.println(number1 <= number2); //true


  
  }
}
 